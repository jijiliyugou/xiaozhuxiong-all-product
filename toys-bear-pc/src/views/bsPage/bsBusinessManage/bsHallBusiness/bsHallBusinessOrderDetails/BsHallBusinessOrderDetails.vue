    // 提交一键加购
    async submit(myData) {
      // this.$common.handlerMsgState({
      //   msg: "敬请期待",
      //   type: "warning"
      // });
      const re = await this.$http.post(
        "/api/AddShoppingCart",
        {
          userID: this.userInfo.userInfo.id,
          companyNumber: this.userInfo.commparnyList[0].companyNumber,
          sourceFrom: "active",
          // sourceFrom: "QRCodeSearch",
          number: 1,
          currency: "￥",
          Price: 0,
          shopType: "companysamples",
          productNumber: myData.productNumber
        },
        {
          timeout: 9999999
        }
      );
      if (re.data.result.code === 200) {
        this.$common.handlerMsgState({
          msg: re.data.result.msg,
          type: "success"
        });
      } else {
        this.$common.handlerMsgState({
          msg: re.data.result.msg,
          type: "danger"
        });
      }
      this.addPurchaseDialog = false;
    },
    // 关闭加购
    close() {
      this.addPurchaseDialog = false;
    },
    // 一键加购
    async openAdd() {
      this.addPurchaseDialog = true;
    },
    // 去厂商
    toFactory(item) {
      const fd = {
        name: item.supplierNumber,
        linkUrl: "/bsIndex/bsVendorQuery",
        component: "bsMyClientsDetail",
        refresh: true,
        noPush: true,
        label: item.supplierName,
        value: {
          companyNumber: item.supplierNumber,
          companyLogo: item.supplierPersonnelLogo,
          companyName: item.supplierName,
          contactsMan: item.supplierPersonnelName,
          phoneNumber: item.supplierPhone,
          address: item.supplierAddres || item.supplierAddress
        }
      };
      this.$store.commit("myAddTab", fd);
      this.$router.push("/bsIndex/bsVendorQuery");
    },
    // 产品详情
    goDetails(row) {
      if (!row.productNumber) {
        this.$common.handlerMsgState({
          msg: "该产品没有产品编号productNumber, 请联系管理员",
          type: "danger"
        });
        return false;
      }
      const fd = {
        name: row.productNumber + row.fa_no,
        linkUrl: "/bsIndex/bsProductSearchIndex",
        component: "bsProductDetails",
        refresh: true,
        label: row.fa_no || "产品详情",
        value: row
      };
      this.$router.push("/bsIndex/bsProductSearchIndex");
      this.$store.commit("myAddTab", fd);
    },
    // 打开选择导出模板
    openSelectTemplate() {
      this.exportTemplateDialog = true;
    },
    // 获取订单详情总数
    async getERPOrderTotal() {
      const res = await this.$http.post("/api/GetERPOrderTotal", {
        id: this.item.erpOrderID
      });
      if (res.data.result.code === 200) {
        this.options = res.data.result.item;
        this.summaryData.totalDegree = this.totalCount;
        this.summaryData.totalQuantity = this.options.sumAmountOu_lo;
        this.summaryData.totalBulkStere = this.options.sumBulk_stere;
        this.summaryData.totalBulkFeet = this.options.sumBulk_feet;
        this.summaryData.totalGrWe = this.options.sumGr_we;
        this.summaryData.totalNeWe = this.options.sumNe_we;
        this.summaryData.sumAmountFa_pr = this.options.sumAmountFa_pr;
        this.summaryData.sumHa_in_qu = this.options.sumHa_in_qu;
      } else {
        this.$common.handlerMsgState({
          msg: res.data.result.msg,
          type: "danger"
        });
      }
    },
    // 获取分享客户订单
    async getSearchCompanyShareOrderDetailsPage() {
      const res = await this.$http.post("/api/GetERPOrderDetailPage", {
        skipCount: this.currentPage,
        maxResultCount: this.pageSize,
        id: this.item.erpOrderID
      });
      if (res.data.result.code === 200) {
        this.tableData.data = res.data.result.item.items;
        this.totalCount = res.data.result.item.totalCount;
      } else {
        this.$common.handlerMsgState({
          msg: res.data.result.msg,
          type: "danger"
        });
      }
      this.getERPOrderTotal();
    },
    // 切换当前页
    currentChange(page) {
      this.currentPage = page;
      this.getSearchCompanyShareOrderDetailsPage();
    },
    // 切换当前页条数
    handleSizeChange(pageSize) {
      this.pageSize = pageSize;
      if (
        this.currentPage * pageSize > this.totalCount &&
        this.currentPage != 1
      )
        return false;
      this.getSearchCompanyShareOrderDetailsPage();
    }
  },
  created() {},
  filters: {
    switchMessageExt(val) {
      let msg;
      switch (val) {
        case "0":
          msg = "系统通知";
          break;
        case "3":
          msg = "补样";
          break;
        case "5":
          msg = "借样";
          break;
        case "11":
          msg = "补样借样";
          break;
        case "12":
          msg = "洽谈";
          break;
      }
      return msg;
    }
  },
  mounted() {
    this.getSearchCompanyShareOrderDetailsPage();
  },
  computed: {
    ...mapState(["userInfo"])
  }
};
</script>
